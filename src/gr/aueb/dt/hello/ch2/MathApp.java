package gr.aueb.dt.hello.ch2;

/**
 * This app uses the Math class of Java.
 */
public class MathApp {

    public static void main(String[] args) {
        System.out.println("The absolute of -5 is: " + Math.abs(-5));
        System.out.println("The min between 1 and 10 is: " + Math.min(1, 10));
        System.out.println("The max between 9, 12 is " + Math.max(9, 12));
    }
}


